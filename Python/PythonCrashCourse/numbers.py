#integers: you can add(+), subtract(-), multiply(*), and divide(/) integers, and the result will be an integer.
addition = 2 + 3
print(addition)

subtraction = 5 - 2
print(subtraction)

multiplication = 2 * 3
print(multiplication)

division = 6 / 2
print(division)
#exponentiation: you can use the ** operator to raise a number to a power.
squared = 3 ** 2
print(squared)
cubed = 2 ** 3
print(cubed)
#order of operations: Python follows the order of operations (parentheses, exponents, multiplication and division, and addition and subtraction) when evaluating expressions.
result = 2 + 3 * 4
print(result)
result = (2 + 3) * 4
print(result)
#python calls any number with a decimal point a floating-point number, or float for short.
float_division = 7 / 2
print(float_division)